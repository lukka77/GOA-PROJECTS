function compareNums(num1, num2) {
    console.log(num1 > num2)
    console.log(num1 >= num2)
    console.log(num1 < num2)
    console.log(num1 <= num2)
    console.log(num1 == num2)
    console.log(num1 === num2)
    console.log(num1 != num2)
    console.log(num1 !== num2)
}

compareNums(10, 3)
compareNums("2", 2)
compareNums(4, 5)