# აქ არის ფუნქცია სახელწოდებით past, რომელიც იღებს სამ პარამეტრს:h-საათი, m-წუთი და s-წამი. ამ პარამეტრების დახმარებით ფუნქცია ითვლის რამდენი მილიწამი არის გავლის დრო.

